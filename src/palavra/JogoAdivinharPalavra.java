    private void botInserirPalavraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botInserirPalavraActionPerformed
        String p = JOptionPane.showInputDialog(this,"Introduza a palavra a adivinhar:");
        palavra = new AdivinharPalavra(p);
        etPalavra.setText(palavra.obterLetrasAdivinhadas());
        tentativas = 10;
        etRestantes.setText(""+tentativas);
    }//GEN-LAST:event_botInserirPalavraActionPerformed

    private void botLetraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botLetraActionPerformed
        if (tentativas > 0) {
            String p = JOptionPane.showInputDialog(this,"Letra:");
            if (palavra.adivinharLetra(p.charAt(0))) {
                etPalavra.setText(palavra.obterLetrasAdivinhadas());
            }
            else {
                tentativas--;
                etRestantes.setText(""+tentativas);
            }

            if (palavra.palavraAdivinhada()) {
                JOptionPane.showMessageDialog(this,"Parab√©ns!");
            }
            else
                if (tentativas == 0) {
                    JOptionPane.showMessageDialog(this,"Perdeu!");    
                }
        }
    }//GEN-LAST:event_botLetraActionPerformed

